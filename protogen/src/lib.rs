fn to_type_parts(desc: &MessageDescriptor) -> Result<(Path, Ident)> {
    dbg!(desc);

    let package = parse_str(desc.package_name().replace(".", "::").as_str())?;
    let name = parse_str(desc.name())?;

    Ok((package, name))
}

fn decode<T: Message + Default>(value: &DynamicMessage) -> Option<T> {
    T::decode(value.encode_to_vec().as_slice()).ok()
}

fn hash(data: &[u8]) -> u32 {
    data.into_iter().fold(0x811C9DC5, |acc, c| (acc ^ (*c as u32)).wrapping_mul(0x1000193))
}

#[derive(Default)]
struct MethodInfo {
    /// The method. This can either be its declaration (eg a trait method) or its implementation.
    implementation: TokenStream,
    dependencies: Vec<String>,
}

/// A package combines source as an AST, and the relative path of the module that contains it.
/// In its purest form, this is just a file that has not yet been emitted to disk.
struct Package {
    source: TokenStream,
    module: Vec<String>,
}

/// Holds a type as well as the package that contains it.
#[derive(Eq, PartialEq, Ord, PartialOrd, Hash)]
struct DependencyInfo {
    /// The complete path where this type lives.
    path: String,

    /// The name of this type.
    typename: String
}

impl DependencyInfo {
    pub fn from(desc: MessageDescriptor) -> Self {
        Self {
            path: desc.package_name().to_string(),
            typename: desc.name().to_string(),
        }
    }
}

impl ToTokens for DependencyInfo {
    fn to_tokens(&self, tokens: &mut TokenStream) {
        let path = parse_str::<Path>(self.path.replace(".", "::").as_str())
            .expect("invalid path");
        let name = format_ident!("{}", &self.typename);

        tokens.append_all(quote! {
            use crate::#path::#name;
        });
    }
}

