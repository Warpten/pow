    fn render_service(svc: ServiceDescriptor) -> TokenStream {
        let name = format_ident!("{}", svc.name());
        let full_name = svc.full_name();

        let make_token_hash= |name: &[u8]| {
            let hash = hash(name);
            Literal::from_str(
                &format!("0x{:X}u32", hash)
            ).unwrap()
        };

        let original_hash = find_extension(&svc, "bgs.protocol.service_options")
            .map(|extension: BgsServiceOptions| {
                let hash = make_token_hash(extension.descriptor_name.expect("Missing descriptor name").as_bytes());

                quote! {
                    const ORIGINAL_HASH: u32 = #hash;
                }
            }).unwrap_or(quote! { });

        let name_hash = make_token_hash(full_name.as_bytes());

        let (methods, imports) = svc.methods()
            .map(Self::render_handler)
            .fold((quote! {}, HashSet::new()), |(code, mut imports), (function, paths)| {
                let imports = imports.into_iter()
                    .chain(paths.into_iter())
                    .collect();

                (quote! {
                    #code

                    #function
                }, imports)
            });

        let imports: Vec<_> = imports.into_iter()
            .sorted()
            .collect();

        // Generate parsers and handler calls.
        let parse_blocks: Vec<_> = svc.methods()
            .filter_map(Self::render_parse_block)
            .collect();

        quote! {
            #(#imports)*

            pub trait #name {
                #original_hash
                const NAME_HASH: u32 = #name_hash;

                #methods

                pub fn call_server_method(&mut self, token: u32, method: u32, stream: &mut Source)
                    -> impl Future<Output = Result<()>>
                {
                    match (method & 0x3FFFFFFFu32) {
                        #(#parse_blocks),*
                        _ => {
                            self.send_error_response("ERROR_RPC_INVALID_METHOD")
                        }
                    }
                }
            }
        }
    }

    fn render_parse_block(md: MethodDescriptor) -> Option<TokenStream> {
        find_extension(&md, "bgs.protocol.method_options")
            .and_then(|o: BgsMethodOptions| o.id)
            .map(|method_id| {
                let input = format_ident!("{}", md.input().name());
                let handler_name = format_ident!("handle_{}", md.input().name());


                // Externally provided arguments: `method` and `token`.
                quote! {
                    #method_id => {
                        match <#input>::decode(buffer) {
                            Ok(request) => {
                                match #handler_name(request) {
                                    Ok(response) => todo!("Send response body"),
                                    Err(status) => {
                                        todo!("Send the status code")
                                    }
                                }
                            },
                            Err(_) => {
                                todo!("ERROR_RPC_MALFORMED_REQUEST")
                            }
                        }
                    }
                }
            })
    }

    /// Renders a [`MethodDescriptor`] into a [`TokenStream`] as well as a collection of all packages
    /// and types needed.
    fn render_handler(md: MethodDescriptor) -> (TokenStream, Vec<DependencyInfo>) {
        assert!(!md.is_client_streaming() && !md.is_server_streaming());

        let (_, input_name) = to_type_parts(&md.input())
            .expect("Failed to parse input type");
        let (_, output_name) = to_type_parts(&md.output())
            .expect("Failed to parse output type");

        let handle_name = format_ident!("Handle{}", md.name());

        let input = DependencyInfo::from(md.input());
        let output = DependencyInfo::from(md.output());

        (quote! {
            fn #handle_name(&mut self, msg: #input_name) -> impl Future<Output = Result<#output_name>> {
                async {
                    self.send_error_code("ERROR_RPC_NOT_IMPLEMENTED")
                }
            }
        }, vec![input, output])
    }

